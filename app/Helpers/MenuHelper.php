    /**
     * Verificar si el usuario tiene permiso para ver un item
     */
    public static function canView($permission) {
        if ($permission === null) {
            return true;
        }

        $user = Auth::user();

        if (!$user) {
            return false;
        }

        return $user->can($permission);
    }
